package com.ntst.last; // 定义包名，用于组织类

public class ParkingFeeCalculator { // 定义一个名为ParkingFeeCalculator的公共类
    public static void main(String[] args) { // 主方法，程序的入口
        double hours = 6.5; // 定义变量hours，表示停车时间，单位为小时
        double fee; // 定义变量fee，用于存储停车费用

        // 根据停车时间计算停车费用
        if (hours <= 1) { // 如果停车时间小于等于1小时
            fee = hours * 5; // 前1小时的费用为5元/小时
        } else if (hours <= 4) { // 如果停车时间在1到4小时之间
            fee = 5 + (hours - 1) * 4; // 前1小时5元，之后每小时4元
        } else { // 如果停车时间超过4小时
            fee = 5 + 3 * 4 + (hours - 4) * 3; // 前1小时5元，接下来3小时每小时4元，超过4小时后每小时3元
        }

        // 输出停车时间和费用
        System.out.printf("停车时间: %.1f 小时, 停车费用: ¥%.2f%n", hours, fee);
        // 使用printf格式化输出，%.1f表示保留一位小数的浮点数，¥%.2f表示保留两位小数的浮点数并加上货币符号¥
    }
}
/*某停车场的收费规则如下：
停车不超过1小时，按5元/小时计费。
停车超过1小时但不超过4小时，前1小时按5元计费，超出部分按4元/小时计费。
停车超过4小时，前1小时按5元计费，接下来的3小时按4元/小时计费，超出4小时的部分按3元/小时计费。
请编写一个Java程序，给定用户停车的时间（以小时为单位），计算并打印总停车费用。 */
